package com.mantou.article.bean;

public class SuccessfulCase {
    private Long id;
    private String owner;
    private String successPosition;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }

    public String getSuccessPosition() {
        return successPosition;
    }

    public void setSuccessPosition(String successPosition) {
        this.successPosition = successPosition;
    }
}
